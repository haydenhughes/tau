class Vector3D:
    """An object for storing a point on a 3D plane.

    :Parameters:
        x : int
        y : int
        z : int
    """

    def __init__(self, x, y, z):
        self.x = x
        self.y = y
        self.z = z


class Vector2D:
    """
    An object for storing a point on a 2D plane

    :Parameters:
        x : int
        y : int
    """

    def __init__(self, x, y):
        self.x = x
        self.y = y
